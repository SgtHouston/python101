# print string with double qoutes
print("Hello World")

# print multi-line messages that begin and end with 3 single quotes.
print('''Hello 
My Name is Chris. ''')

# Print quotations in a string by using the opposite quotes of opening/closing that you do for the inside of the string.
print('Hello "World"')

# Concatenate Strings
print("Hello World, " + "What's up?!")

# Concatenate a string and the result of integer operations as a string.
print("The result of 1 + 1 is " + str(1 + 1))

# integer - whole number
print(1)

# float - fractional number/number with decimal places 
print(2.0)

# Adding integers to floats
print(1 + 2.0)







